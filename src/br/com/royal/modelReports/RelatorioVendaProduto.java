package br.com.royal.modelReports;

public class RelatorioVendaProduto {
	public String dataVendaProduto;
	public double valorTotalVendaProduto;
	public String nomeProduto;
	public float valorProduto;
	
	
	
	public String getDataVendaProduto() {
		return dataVendaProduto;
	}
	public void setDataVendaProduto(String dataVendaProduto) {
		this.dataVendaProduto = dataVendaProduto;
	}
	public double getValorTotalVendaProduto() {
		return valorTotalVendaProduto;
	}
	public void setValorTotalVendaProduto(double valorTotalVendaProduto) {
		this.valorTotalVendaProduto = valorTotalVendaProduto;
	}
	public String getNomeProduto() {
		return nomeProduto;
	}
	public void setNomeProduto(String nomeProduto) {
		this.nomeProduto = nomeProduto;
	}
	public float getValorProduto() {
		return valorProduto;
	}
	public void setValorProduto(float valorProduto) {
		this.valorProduto = valorProduto;
	}


}
